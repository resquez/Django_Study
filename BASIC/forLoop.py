# range() 함수를 사용한 순회
# range(n) : 0부터 n-1까지의 숫자를 생성하는 함수
# range(n, m) : n부터 m-1까지의 숫자를 생성하는 함수
# range(n, m, s) : n부터 m-1까지의 숫자를 생성하는데, s만큼 증가하는 함수

for i in range(5):
    print(i)