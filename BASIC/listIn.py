# in 연산자
# 리스트 안에 특정 값이 존재하는지 확인하는 연산자

fruits = ['apple', 'banana', 'cherry']

# banana가 fruits 리스트에 포함되어 있는 지 확인
if 'banana' in fruits:
    print('바나나가 있습니다.')
else:
    print('바나나가 없습니다.')

# not in 연산자
# 리스트 안에 특정 값이 존재하지 않는 지 확인하는 연산자
if 'orange' not in fruits:
    print('오렌지가 없습니다.')
else:
    print('오렌지가 있습니다.')
