# 기본 리스트

# 빈 리스트 선언
empty_list = []

# 숫자로 구성된 리스트 선언
number_list = [1, 2, 3, 4, 5]

# 문자열로 구성된 리스트 선언
string_list = ["Python", "is", "good"]

# 여러 데이터 타입으로 구성된 리스트 선언
mixed_list = [1, 2, "Python", "is", True]

# 중첩 리스트: 리스트 안에 리스트가 포함도니 형태
nested_list = [[1, 2, 3], [4, 5, 6], ["Python", "is", "good"], [True, False]]